// 카멜(Camel) 기법
// 첫 단어의 첫 글자는 소문자, 이후 다음 단어의 첫 글자부턴 대문자
// 단어와 단어가 연결될 때, 가장 첫 글자를 대문자로 작성
// js, java 같은 언어에서 변수 및 함수 등에 사용

// 예시
// let user
// let userName;
// let userFirstName;

// 파스칼(Pascal) 기법
// 모든 단어의 첫 글자가 대문자
// js, java 같은 언어에서 클래스 등에 사용

// 예시
// let User;
// let UserName;
// let UserFirstName;

// 스네이크(Snake) 기법
// 모든 단어를 소문자로 작성, 단어와 단어 사이를 언더바(_)로 연결
// js, java 같은 언어에서 상수 등에 사용
// C 계열 언어에서 주로 사용

// 예시
// let user
// let user_name
// let user_first_name;

// 케밥(Kebab) 기법
// 모든 단어를 소문자로 작성, 단어와 단어 사이를 하이픈(-)으로 연결
// js, java 같은 언어에서는 사용하지 않음
// HTML, CSS 등에서 주로 사용

// 예시
// let user;
// let user-name;
// let user-first-name;

// 변형 기법
// 상수의 경우 전부 대문자, 단어와 단어 사이를 언더바(_)로 연결

// 예시
// let USER;
// let USER_NAME;
// let USER_FIRST_NAME;